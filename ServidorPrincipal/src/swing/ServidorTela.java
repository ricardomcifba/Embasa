    private void btnServidorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnServidorActionPerformed
        if (evt.getSource() == btnServidor) {
            try {
                JOptionPane.showMessageDialog(null, "Para conectar ao ip: " + InetAddress.getLocalHost().getHostAddress() + " porta: " + textPorta.getText() + "\n click em OK!");
            } catch (java.net.UnknownHostException ex) {

                Logger.getLogger(ServidorTela.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                server = new RMIPrincipal(txtNome.getText(),Integer.parseInt(textPorta.getText()));
                //server.executa();
            } catch (RemoteException ex) {
                Logger.getLogger(ServidorTela.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NotBoundException ex) {
                Logger.getLogger(ServidorTela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnServidorActionPerformed

